function returnSelect(name){
	var valuesData = $('#'+name).multipleSelect('getSelects');
	var v = valuesData.length;
	var returnSelect="";
	if(v==0){//没有选择
		returnSelect = "-1";
	}else{
		var v1 = $('#'+name+' option').length;
		if(v1==v){//全选
			returnSelect = "-1";
		}else{
			returnSelect = valuesData.join(",");                                                                 
		}
	}
	return returnSelect;
}
