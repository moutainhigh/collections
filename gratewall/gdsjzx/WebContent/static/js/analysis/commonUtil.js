function add(num1,num2){
    var n1,n2,m;
    try{
        n1 = num1.toString().split(".")[1].length;
    }catch(e){
        n1 = 0;
    }
    try{
        n2 = num2.toString().split(".")[1].length;
    }catch(e){
        n2 = 0;
    }
    m = Math.pow(10,Math.max(n1,n2));
    return (num1*m + num2*m)/m;
}

/**
 * 获取多层multSelect值，默认需要有 “全选(value=-1)”的选择
 * 父集与子集需要通过名字为p的属性关联
 * request: 请求的data数据(json)格式
 * selectNames: 数组，select各个层名字[P1name, p2name, p3name...]
 * valueNames: 数组，各个层返回值名字[v1name, v2name, v3name...]
 */
function getMultSelectedValue(request, selectNames, valueNames, index, lastValue) {
	var t = "";
//	var before = "";
	for(var i = 0; i < selectNames.length; i ++){
		if((t = returnSelect(selectNames[i])) != '-1'){//非全选
			request[valueNames[i]] = t;
		}
	}
	return ;
	//保留
	var count = 0;
	$("#" + selectNames[index]).next().find("label[class='checked'] input").each(function(index,element){ 
		if(element.value == '-1'){ //全部无须赋值
			t = "-1";
			return false ;
		}
		count ++;
		t += element.value + ','; 
	});
	if(t == "-1" || t == ""){//全选||未选择,获取上一层至
		if(index > 0){
			request[valueNames[index - 1]] = lastValue;
		}
	}else if(count == 1){ //只选择一个且不为全选
		getMultSelectedValue(request, selectNames, valueNames, index + 1, t);
	}else{//当前层
		request[valueNames[index]] = t;
	}
}

