package com.gwssi.entSelect.pojo;

public class EntErrorLog {

	private String id;
	private String maintbid;
	private String pripid;
	private String regino;
	
	private String date;
	private String msg;
	
	private String itemcode;
	private String logDetail;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMaintbid() {
		return maintbid;
	}
	public void setMaintbid(String maintbid) {
		this.maintbid = maintbid;
	}
	public String getPripid() {
		return pripid;
	}
	public void setPripid(String pripid) {
		this.pripid = pripid;
	}
	public String getRegino() {
		return regino;
	}
	public void setRegino(String regino) {
		this.regino = regino;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getItemcode() {
		return itemcode;
	}
	public void setItemcode(String itemcode) {
		this.itemcode = itemcode;
	}
	public String getLogDetail() {
		return logDetail;
	}
	public void setLogDetail(String logDetail) {
		this.logDetail = logDetail;
	}
	
	
	
	
}
